<script>
    // 各スライダーに対して個別にサムネイルを生成
    $('.ms_img').each(function() {
        var $slider = $(this).find('.js-slider-6');
        var $thumbsContainer = $(this).find('.thumbs_dots');
        var thumbnails = '';

        $slider.find('li').each(function(index) {
            var thumbSrc = $(this).find('img').attr('src');
            thumbnails += '<li data-index="' + index + '"><img src="' + thumbSrc + '" alt="Thumbnail ' + (index + 1) + '"></li>';
        });

        $thumbsContainer.html('<ul class="thumbs_list">' + thumbnails + '</ul>');

        // メインスライダーの設定
        $slider.slick({
            autoplaySpeed: 3000,        // 自動再生の速度（ミリ秒）
            dots: false,                // ドットナビゲーションを表示しない
            arrows: false,              // 矢印ナビゲーションを非表示にする
            fade: true,                 // フェードイン/アウトのトランジションを使用する
        });

        // サムネイルクリック時にメインスライダーを制御
        $thumbsContainer.on('click', 'li', function() {
            var index = $(this).data('index');
            $slider.slick('slickGoTo', index);

            // サムネイルのactiveクラスを管理
            $thumbsContainer.find('li').removeClass('slick-active');
            $(this).addClass('slick-active');
        });

        // 初期表示のサムネイルにactiveクラスを付与
        $thumbsContainer.find('li').eq(0).addClass('slick-active');

        // スライダー変更時にサムネイルのactiveクラスを管理
        $slider.on('afterChange', function(event, slick, currentSlide) {
            $thumbsContainer.find('li').removeClass('slick-active');
            $thumbsContainer.find('li').eq(currentSlide).addClass('slick-active');
        });
    });
</script>


